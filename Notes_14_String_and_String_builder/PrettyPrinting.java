package Notes_14_String_and_String_builder;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 56778.34299423442322f;

        System.out.printf("Formatted number is : %.2f", a); // Formatted number is : 56778.34
        System.out.println();

        System.out.println(Math.PI); // 3.141592653589793
        System.out.printf("Pie : %.3f", Math.PI); // Pie : 3.142

        System.out.printf("I am %s and I am %s", "Aman Rajput", "Cool."); // I am Aman Rajput and I am Cool.

    }
}
